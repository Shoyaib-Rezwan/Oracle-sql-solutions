SELECT EMPLOYEE_ID FROM EMPLOYEES
MINUS(
SELECT MANAGER_ID FROM DEPARTMENTS
)
ORDER BY EMPLOYEE_ID;

--(B)
SELECT EMPLOYEE_ID,LAST_NAME FROM EMPLOYEES E
MINUS(
SELECT D.MANAGER_ID,(
SELECT LAST_NAME FROM EMPLOYEES E
WHERE E.EMPLOYEE_ID=D.MANAGER_ID
) FROM DEPARTMENTS D
)
ORDER BY EMPLOYEE_ID;

